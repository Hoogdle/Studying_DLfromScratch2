### 신경망 추론 전체 그림
# 신경망을 간단히 말하면 단순한 '함수' 라고 할 수 있다.
# 완전신경망 : 인접하는 층의 모든 뉴런과 연결 되어 있는 신경망
# 은닉층의 뉴런은 가중치의 합으로 계산된다.

# 미니배치 구현 식
# x : Nx2 (N: 데이터 갯수)
# W : 2x4
# h : Nx4
# h = xW + b

import numpy as np
W1 = np.random.randn(2,4) #가중치
b1 = np.random.randn(4) #편향
x = np.random.randn(10,2) # 입력
h = np.matmul(x,W1)+b1
# 10개의 샘플 데이터 각각을 완전연결계층으로 변환
# x[0] : 0번 째 입력 데이터
# x[1] : 1번 째 입력 데이터
# h[0] : 0번 째 은닉층 뉴런
# h[1] : 1번 째 은닉층 뉴런