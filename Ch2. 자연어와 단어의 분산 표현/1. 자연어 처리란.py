### 자연어 처리란 ###
# 자연어 : 우리가 평소에 ㅆ느느 말
# 자연어 처리(Natural Language Processing - NLP) : 우리의 말을 컴퓨터에게 이해시키기 위한 기술(분야)
# 컴퓨터는 프로그래밍 언어를 알아듣지만 프로그래밍 언어는 딱딱한 언어인 반면, 우리의 말은 부드러운 언어이다.
# 자연어 처리 기술 예시 : 검색엔진, 기계 번역, 질의응답 시스템,IME(입력기 전환),문장 자동요약, 감정분석 등

### 단어의 의미
# 우리말은 문자로 구성되며 말의 의미는 단어로 구성된다. 즉 단어는 의미의 최소 단위이다.
# 자연어를 컴퓨터에게 이해시키는데 있어서 '단어의 의미'를 이해시키는 것이 매우 중요하다

# 단어의 의미를 잘 파악하는 표현 방법 
# 시소러스(Thesaurus, 사람의 손으로 만듦)
# 통계 기반 기법(통계 정보 이용)
# 추론 기반 기법(word2vec)

